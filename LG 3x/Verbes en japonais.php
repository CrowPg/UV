<div class="homonymie"><a href="/wiki/Aide:Homonymie" title="Aide:Homonymie"><img alt="Page d'aide sur l'homonymie" src="http://upload.wikimedia.org/wikipedia/commons/thumb/3/3e/Disambig_colour.svg/20px-Disambig_colour.svg.png" width="20" height="15" /></a> Pour les articles homonymes, voir <a href="/wiki/Racine">Racine</a>.</div>
<p>En <a href="/wiki/Linguistique">linguistique</a>, un <b>radical</b> (ou <i>racine</i> ou encore <i><a href="/wiki/Morph%C3%A8me">morphème</a> lexical</i>, <i><a href="/wiki/Lex%C3%A8me">lexème</a></i>, bien que ces notions ne soient pas, dans le détail, identiques) est la plus petite et <i>plus ancienne</i> unité <a href="/wiki/Lexique" title="Lexique">lexicale</a> qui permette de former des <a href="/wiki/Mots_apparent%C3%A9s" class="mw-redirect" title="Mots apparentés">mots apparentés</a>. Cette notion est principalement étudiée en <a href="/wiki/Morphologie_(linguistique)" title="Morphologie (linguistique)">morphologie</a>, <a href="/wiki/%C3%89tymologie" title="Étymologie">étymologie</a> et <a href="/wiki/Linguistique_compar%C3%A9e">linguistique comparée</a> mais aussi en <a href="/wiki/Lexicologie">lexicologie</a> (bien que l'on se réfère de préférence plus facilement au <a href="/wiki/Lex%C3%A8me">lexème</a> dans ce dernier cas). Si <i>lexème</i> connote une analyse <a href="/wiki/Synchronie" title="Synchronie" class="mw-redirect">synchronique</a>, <i>radical</i> est plus souvent réservé à l'<i>analyse diachronique</i>.</p>
<p>Le radical n'est pas forcément un <a href="/wiki/Lemme_(linguistique)" title="Lemme (linguistique)">lemme</a>, d'autant moins dans les <a href="/wiki/Langue_flexionnelle" title="Langue flexionnelle">langues flexionnelles</a>, surtout anciennes. En <a href="/wiki/Latin">latin</a>, par exemple, il n'existe que très peu de <i><a href="/w/index.php?title=Mot-racine&amp;action=edit&amp;redlink=1" class="new" title="Mot-racine (page inexistante)">mots-racines</a></i> (mots, ou <a href="/wiki/Lemme_(linguistique)" title="Lemme (linguistique)">lemmes</a>, ne comportant qu'un radical nu, sans <a href="/wiki/Affixe">affixe</a>). Le radical <a href="/wiki/D%C3%A9notation" title="Dénotation" class="mw-redirect">dénote</a> une notion simple que la <a href="/wiki/D%C3%A9rivation">dérivation</a> permettra de préciser, au moyen d'affixes, par exemple. Dans les <a href="/wiki/Langues_indo-europ%C3%A9ennes">langues indo-européennes</a> anciennes, le radical est le plus souvent <a href="/wiki/Verbe" title="Verbe">verbal</a>.</p>
<p>C'est par dérivation d'un radical que l'on obtient des mots d'une autre <a href="/wiki/Classe_lexicale" class="mw-redirect" title="Classe lexicale">classe lexicale</a> restant <a href="/wiki/Mots_apparent%C3%A9s" title="Mots apparentés" class="mw-redirect">apparentés</a>. Un radical augmenté de suffixes devient un <a href="/wiki/Th%C3%A8me_morphologique">thème morphologique</a>, lequel devient un <a href="/wiki/Lemme_(linguistique)" title="Lemme (linguistique)">lemme</a> une fois qu'on y a adjoint des désinences voulues le cas échéant.</p>
<p>La notion de racine est aussi très importante dans les <a href="/wiki/Langues_s%C3%A9mitiques">langues sémitiques</a> car les procédés de dérivation y sont bien plus systématiques et cohérents que dans les langues indo-européennes naturelles. C'est le cas naturellement en <a href="/wiki/Arabe">Arabe</a>, langue qui entretient de ce point de vue avec l'<a href="/wiki/H%C3%A9breu">Hébreu</a> une géméllité remarquable, et qui fait que les deux langues s'expliquent, de ce point de vue, mutuellement l'une par l'autre.</p>
<p>Dans l'analyse des langues modernes, on nomme parfois <i>radicaux</i> les éléments de <a href="/wiki/Mot_compos%C3%A9" title="Mot composé">mots composés</a>. C'est le cas pour les mots savants formés à partir de prétendus <a href="/wiki/Racine_grecque" title="Racine grecque" class="mw-redirect">radicaux grecs</a> ou <a href="/wiki/Latin" title="Latin">latins</a>, comme <i>télécommande</i>, formé à partir de l'<a href="/wiki/Adverbe">adverbe</a> <a href="/wiki/Grec_ancien" title="Grec ancien">grec</a> <i>tèle</i>, «&#160;au loin&#160;», et d'un <a href="/wiki/D%C3%A9verbal">déverbal</a> du verbe <i>commander</i>, c'est-à-dire <i>commande</i>. Le verbe <i>commander</i> s'analyse quant à lui en deux pseudo-radicaux, <i>com-</i>, du latin <i>cum</i>, et <i>mander</i>, du latin <i>mandare</i>. Seul <i>mandare</i> est réellement un radical, et encore si on lui ôte le <a href="/wiki/Suffixe">suffixe</a> de formation d'<a href="/wiki/Infinitif">infinitif</a> <i>-are</i>. L'on obtient alors <i>mand-</i>, radical irréductible et, d'ailleurs, verbal, du mot <i>télécommande</i>.</p>
<p>On le voit, le mot <i>radical</i> n'a pas le même sens selon qu'on analyse de manière <a href="/wiki/Diachronie" title="Diachronie" class="mw-redirect">diachronique ou synchronique</a>. Ce qui est un radical dans une langue moderne était peut-être un thème de dérivation dans sa langue-mère. Inversement, les radicaux de la langue-mère peuvent, dans la langue-fille, ne plus être identifiables comme telle. On pourra, pour différencier le radical historique en diachronie de celui permettant de construire des <a href="/wiki/Lemme_(linguistique)" title="Lemme (linguistique)">lemmes</a> en synchronie, qualifier ce dernier de <a href="/wiki/Lex%C3%A8me">lexème</a>.</p>
<p>Par exemple, en dernière analyse, le radical historique du <a href="/wiki/Lemme_(linguistique)" title="Lemme (linguistique)">lemme</a> français <i>anticonstitutionnellement</i> est <a href="/wiki/Latin">latin</a>, c'est <i>sta-</i> (<i>sti-</i> étant un <a href="/wiki/Allomorphe">allomorphe</a>)&#160;: il permet de relier des termes apparemment éloignés comme <i>statue</i>, <i>être</i>, <i>désister</i>, <i>stable</i>, <i>étable</i>, <i>prostitution</i>, <i>rester</i> ou <i>station</i>. D'un point de vue synchronique, le lexème est <i>stitu-</i>, qui permet de relier lexicalement <i>dé-stitu-er</i>, <i>constitu-er</i>, <i>dé-stitu-tion</i>, <i>con-stitu-tionnel</i>, etc. Toujours en synchronie, <i>constitution</i> et <i>stable</i> n'apparaissent pas formés sur un même lexème&#160;: celui de <i>stable</i> est <i>stab(i)l-</i>, que l'on retrouve dans <i>stab-ilité</i> ou <i>stabiliser</i>. Si, historiquement, ils proviennent d'un même ancêtre, actuellement seule l'étude étymologique permet de le savoir&#160;: un locuteur lambda ne s'en rendra pas forcément compte.</p>
<p>Enfin, tous les mots ne sont pas construits à partir d'un radical identifiable, quel que soit l'angle d'approche que l'on adopte. On parle dans ce cas de mots <i>immotivés</i>. La plupart des <a href="/wiki/Mot-outil" title="Mot-outil">mots-outils</a> (les morphèmes grammaticaux libres comme <i>hier</i>, <i>le</i> ou <i>de</i>) sont immotivés.</p>
